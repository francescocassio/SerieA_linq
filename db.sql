-- Crea il database
CREATE DATABASE SerieA;
GO
USE SerieA; 
GO

-- Tabella Squadre
CREATE TABLE Squadre (
    Id INT PRIMARY KEY IDENTITY,
    Nome NVARCHAR(100),
    Città NVARCHAR(100)
);

-- Tabella Giocatori
CREATE TABLE Giocatori (
    Id INT PRIMARY KEY IDENTITY,
    Nome NVARCHAR(100),
    Età INT,
    Ruolo NVARCHAR(50),
    SquadraId INT FOREIGN KEY REFERENCES Squadre(Id)
);

-- Tabella Partite
CREATE TABLE Partite (
    Id INT PRIMARY KEY IDENTITY,
    Data DATE,
    GolCasa INT,
    GolTrasferta INT,
    SquadraCasaId INT FOREIGN KEY REFERENCES Squadre(Id),
    SquadraTrasfertaId INT FOREIGN KEY REFERENCES Squadre(Id)
);

